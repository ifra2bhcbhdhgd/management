public class switchcase {
    public static void main(String[] args) {


        int n = 8;
        switch (n) {
            case 1:
                System.out.println("junary");
                break;
            case  2:
                System.out.println("february");
                break;
            case 3:
                System.out.println("march");
                break;
            default:
                System.out.println("no finding");

        }
    }
}